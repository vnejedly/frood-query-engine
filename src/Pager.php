<?php
namespace Hooloovoo\QueryEngine;

/**
 * Class Pager
 */
class Pager
{
    /** @var int */
    protected $offset;

    /** @var int */
    protected $limit;

    /**
     * @param int $offset
     */
    public function setOffset(int $offset)
    {
        $this->offset = $offset;
    }

    /**
     * @return int
     */
    public function getOffset(): int
    {
        return $this->offset;
    }

    /**
     * @param int $limit
     */
    public function setLimit(int $limit)
    {
        $this->limit = $limit;
    }

    /**
     * @return int
     */
    public function getLimit(): int
    {
        return $this->limit;
    }
}